-- 		time_fmt: if [member.A2ScoreboardQuery.get_date_time] was true, convenience time format in the form HH:MM:SS.SSS
-- 		replay_id: the ID of the "replay" in the database that you use to download the replay, or view the discussion page (even if there
--			is no actual replay file).
-- 		run_achievements: if [member A2ScoreboardQuery.get_run_achievements] was true, an array of "run acheivements" which can be achieved
-- 			during any run. These are dictionaries with the following format:
-- 				username: User who got the achievement (useful for co-op matches)
-- 				achievement: nickname of the achievement (was set up by you through arcade2000's dev control panel)
-- 				frame: if [member A2ScoreboardQuery.get_run_achievement_frames] was true, the exact frame that the achievement was obtained
-- 					on, provided by you.
function M.web_get_scoreboard(scoreboard_query, level, player_count, relative_to_user, friends_only, order_by_wins)
	if level == nil then level = "" end
	if player_count == nil then player_count = 1 end
	if relative_to_user == nil then relative_to_user = true end
	if friends_only == nil then friends_only = false end
	if order_by_wins == nil then order_by_wins = false end


	local func_name = "web_get_scoreboard"
	local original_level = level

	M.last_scoreboard = nil   -- until we get a new one
	
	if level == "" then
		level = "_"
	end

	-- Build the URL from the following format:
	-- a2/api/game/<slug:game_nickname>/lvl/<slug:level_nickname>/plrs/<int:players>/top_scores/<int:perpage>/<signedint:page>
	local level_slug = level
	if level_slug == "" then level_slug = "_" end
	local url = A2Settings.ARCADE2_BASEURL .. "a2/api/game/" .. A2Settings.GAME_NAME .. "/lvl/" .. level_slug .. "/plrs/" .. tostring(player_count) .. "/top_scores/"

	local query_params = ""

	if friends_only then
		query_params = query_params .. "&friends=true"
	end

	if order_by_wins then
		query_params = query_params + "&order_by_wins=true"
	end

	if A2Settings.STEAMWORKS then
		query_params = queery_params .. "&steam=true" -- content may need to be catered to Steam to match their guidelines, versus a non steam version
	end

	if #query_params > 0 then
		query_params = "?" .. string.sub(query_params, 2)  -- remove first "&"
		url = url ..query_params
	end

	if scoreboard_query == nil then
		scoreboard_query = A2ScoreboardQuery.new()
	end

	if relative_to_user and #M.get_users() > 0 and #scoreboard_query.center_on == 0 then
		scoreboard_query.center_on = M.get_users() -- if relative_to_user is true, we center on the logged in user(s) unless otherwise specified
	elseif not relative_to_user then
		scoreboard_query.center_on = {} -- array  -- if relative_to_user is false, we don't center on anyone
	end

	-- Convert scoreboard_query to JSON
	local message = json.encode(scoreboard_query)

	-- POST request to URL
	_new_request(func_name)
	local callback = _G["_complete_" .. func_name]

	local pass_to_callback = {
		query=scoreboard_query, -- remember this for the completion handler
		level=original_level,
		player_count=player_count,
		relative_to_user=relative_to_user,
		friends_only=friends_only,
		order_by_wins=order_by_wins
	}


	https.request({url, {
			headers = {["Content-Type"] = "application/json"},
			data = message,
			method = "POST"
		}}, callback, pass_to_callback)
end


