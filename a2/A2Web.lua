--
-- This is the main interface for dealing with the arcade2000.io website.
-- It allows you to register scores, replays, clips, and achievements offline,
-- then synchronize them to the arcade2000.io website, all in a secure way that
-- ensures nobody can spoof a score by simply changing the datafiles.
-- Please don't share this code publicly,
-- since although arcade2000 uses secure hashes to help ensure authenticity, it's impossible to prevent cheating
-- entirely, since every player has full authority over the memory on their computer. Hacking games using
-- memory tools is easier the more is known about their inner workings.
-- Also, this raw file should not be included in your Love2D game, it should be obfuscated somehow (I believe this
-- can be done to a sufficient level by precompiling it into Lua bytecode)
-- Feel free to email admin@arcade2000.io with any questions, or if your game has special needs
-- (custom authentication methods, special score chart types etc).
--
-- Most important arcade2000 functions run offline. They store their data in A2Settings.A2_DATA_FILE.
-- It's only once you do a2web.web_sync that the user's scores, replays, clips, and achievements
-- are all synchronized with the server, and if it fails, you can just try again later. Users
-- should always be allowed to play offline, and doing so doesn't mean their scores are invalid.
--
-- Furthermore, they don't even have to type a name for their scores to be recorded. arcade2000 takes a "kick the can
-- down the road" approach, so nothing needs to get in the way of just immediately starting gameplay when your player
-- first runs your game. Their scores and replays can be recorded locally and, if they care, they can type in a username
-- later to claim all their last run and submit it to the online scoreboard. Passwords, too, are optional, since if
-- none is provided the unique machine ID is used.
-- 
-- WEB FUNCTIONS
-- These actually make web requests happen. This happens in a separate thread. You must iterate through a2web.get_web_returns()
-- to see if any of your web functions finished.
--
--
-- PAGE FUNCTIONS
--
-- The intent of these is to return a generated URL which points to a specific resource on arcade2000.io.
-- You can use this to let people go view the online scoreboard (without having to code one in your game,
-- if you don't want to), set up their user profile, manage their clips and replays, download other people's clips/replays,
-- etc. It's also pretty important you have a link to arcade2000 somewhere in your game, because without it, they
-- won't be able to "soft login" and connect their accounts so we can know it's the same player across multiple games.
--
--
-- NOTES ON SYNC AND CHEATING AVOIDANCE
--
-- Basic cheating avoidance is achieved using md5 hashes to ensure the local data
-- was authentically generated by your game, securely linking scores to replay files,
-- etc. In the case of replay files, the hash incorporates the binary data of the actual
-- replay file, so a specific replay is rigidly linked to the username and score. The user
-- cannot spoof a score by simply changing the JSON, because the hash would be detected
-- as wrong on the server side.
--
-- If you want to let your players start the game without typing in a username, you can.
-- Their scores and replays will be "signed" with their machine ID, and as soon as a valid
-- username is chosen, the hashes are recalculated (assuming machine ID matches) and all
-- the data is assigned to the new username. They need a username to do web_sync, but they
-- do not need a password; machine ID is used as a password until they explicitly set one.
-- They can even browse and modify their arcade2000.io account online by calling
-- functions like a2web.open_page_user, though note that machine ID will be passed
-- as a URL parameter which is slightly insecure.
-- 
-- The user should create a real password, at which point the browser requires a proper
-- login to modify anything and doesn't put password in the URL parameter.
-- Note that if they claim a username on one machine, they need to set a password in order
-- to be able to use the same username on another machine.


-- Passwords keyed by username.
-- It's not stored in A2Metadata, because it's not stored on the hard drive.
-- Instead, on next web sync, it's eventually used to obtain a device-specific
-- token which is stored in A2Metadata.get_data().tokens, and the password itself
-- is forgotten. If they exit without doing web_sync, they'll have to enter the
-- password again.
