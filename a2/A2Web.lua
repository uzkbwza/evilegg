-- For multiplayer games, for which you want co-op score charts,
-- log in multiple users at once. Works like a2web.set_user, but for multiple users.
-- users_list is in the following format:
-- {{"username":"user1", "password":"pwd1", "email":""}, {"username":"user2", "password":"pwd2", "email":"my@email.com"}}
function M.set_multi_users(users_list)
	-- Remember these users for future reference, not forgetting any additional players who were selected.
	
	-- keep track of any users we replace and stick them at the end of the list.
	-- they just get pushed back, but not forgotten
	local forgotten_users = {}
	
	for i= 1,#users_list do
		local user = users_list[i]
		
		-- password is stored in memory, not on the hard drive
		_passwords[user["username"]] = user["password"]
		_emails[user["username"]] = user["email"]
		
		
		if #A2Metadata.get_data().multiplayer_users <= i then  -- nothing here in multiplayer_users yet
            table.insert(A2Metadata.get_data().multiplayer_users, user["username"])
		else  -- there is something here. 
			if A2Metadata.get_data().multiplayer_users[i] ~= user["username"] then  -- we're clobbering another user
				table.insert(forgotten_users, A2Metadata.get_data().multiplayer_users[i]) -- scoot them to the end
            end
			A2Metadata.get_data().multiplayer_users[i] = user["username"]
        end
    end

	-- Now add the forgotten users to the end of the list. We'll keep a running list of everyone who's ever
	-- logged in, unless you do logout_user.
	for i, username in ipairs(forgotten_users) do
		if not A2Util.array_contains(A2Metadata.get_data().multiplayer_users, username) then
			table.insert(A2Metadata.get_data().multiplayer_users, username)
        end
    end
	
	-- Meanwhile, users can just be a simple list of usernames.
	-- set_num_players will refresh it.
	M.set_num_players(#users_list)   -- this will result in an A2Metadata.save() call
	
	-- If a register_score has been buffered, we should process it now (user typed name AFTER getting the score)
	if #users_list > 0 and buffered_score_submission ~= nil then
		local result = M.register_score(
			buffered_score_submission.score,
			buffered_score_submission.frame_length,
			buffered_score_submission.level,
			buffered_score_submission.replay_file,
			buffered_score_submission.mini_replay_file, 
			buffered_score_submission.subscores,
			buffered_score_submission.meta_str,
			buffered_score_submission.cheating_detected)
		if result ~= -1 then
			buffered_score_submission = nil
        end
    end
end


-- For any player that ever gets synced or web_check_username'd, we keep a device-specific login token for
-- them. This allows us to sync their data even if they aren't the currently logged in player. There is no
-- known security concerns with storing tokens indefinitely, since they only work on one machine, so you
-- only need to provide logout functionality if you think your players would appreciate it.
function M.logout_user(username)
	A2Metadata.get_data().tokens[username] = nil  -- forget their login token
	
	-- Remove the user from currently active users (they may or may not be in here)
	for i=#A2Metadata.get_data().users-1, 1, -1 do
		if A2Metadata.get_data().users[i] == username then
			A2Metadata.get_data().users[i] = nil
        end
    end
	
	A2Metadata.save()
end


-- This is automatically set on a call to a2web.set_user or a2web.set_multi_users, based
-- on the number of players in the call.
-- If you call this explicitly, you can bring back the previously used players in multiplayer without
-- having to remember them yourself. So for exammple, when player 2 joins, just set_num_players to 2
-- and then a2web.get_users will tell you if there's already a default used in that slot.
function M.set_num_players(players)
	A2Metadata.get_data().users = {} -- array
	for i = 1,players do   -- add previous multiplayer_users up to count of players
		if i <= #A2Metadata.get_data().multiplayer_users then
			table.insert(A2Metadata.get_data().users, A2Metadata.get_data().multiplayer_users[i])
		end
	end
	
	A2Metadata.save()
end

