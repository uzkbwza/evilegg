-- once. If you don't get a successful A2Web.web_check_username or A2Web.web_check_usernames
-- at least once for a user, you have no guarantee that they'll be able to submit their scores (although
-- their scores will still be saved locally, and it would be possible to submit them later if they
-- eventually get a valid login).
-- If a username does not exist, it is automatically created with the supplied password.
