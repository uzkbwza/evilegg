-- Will return an absolute link from a relative link, while
-- also appending query params like the user's device ID
function M.get_web_link(relative_link)
	local ret = A2Settings.ARCADE2_BASEURL .. relative_link 
	
	-- append required trailing slash
	if string.sub(relative_link, -1) ~= "/" then
		relative_link = relative_link .. "/"
	end
	
	-- add soft login information
	
	if #M.get_users() == 1 then   -- don't bother doing soft login if more or less than 1 user	
		local username = M.get_users()[1]
		local machine_id = M.get_machine_id()
		ret = ret .. "?username=" .. username .. "&device_id=" .. machine_id .. "&soft_login_game=" .. A2Settings.GAME_NAME
	
		if A2Settings.STEAMWORKS then
			-- See if the "steam-" user is logged in. If not, allow non-steam users to soft login from the same device.
			if string.sub(M.get_users()[1], 1, 6) == "steam-" then
				if A2Metadata.get_data().soft_login_token then
					ret  = ret .. "&token=" + A2Metadata.get_data().soft_login_token  -- token can be used for soft login, since device id will not be enough for Steam. this token is a soft login token generated from the steam authentication ticket.
				end
			end
			
			ret =  ret .. "&steam=true" --content may need to be catered to Steam to match their guidelines, versus a non steam version
		end
	else
		if A2Settings.STEAMWORKS then
			ret = ret .. "?steam=true" --content may need to be catered to Steam to match their guidelines, versus a non steam version
		end
	end
	
	return ret
end



