function _complete_web_post_score(response_code, body)
	local func_name = "web_post_score"
	local generic_wr = _generic_complete_web(response_code, body, func_name)
	if generic_wr ~= nil then  -- something basic went wrong
		_got_web_return( generic_wr )
		return
	end
	
	-- note if this fails, score.web_id will remain unset, which means we'll try again next time
	-- web_sync occurs.
	
	-- if we got back a web ID, we can add it to the score, and it will never need to be synced again.
	-- Note this is the expected return from the python side:
	-- JsonResponse({'score_id': new_score.id, 'clip_id': new_clip.id, 'message': 'Score submitted successfully.'})
	local txt = body
	A2Print.print("Response for post score :" .. txt )
	local json = json.decode(txt)
	if json == nil or json["score_id"] == nil or json["local_score_id"] == nil then
		if json['message'] ~= nil then
			local error = A2ErrorReturn.new(func_name, A2ErrorReturn.A2ErrorCode.BAD_REQUEST, "Server error. Please try again later.", "Error: \"" .. json['message'] .. "\" Check payload for details ")		
			_got_web_return(A2WebReturn.new(func_name, 1.0, error, body))
		else
			_got_web_return(A2WebReturn.json_parse_error(func_name))
		end
		return
	end
		
	
	-- search for the local_score_id in our scores, then update its web id
	local found_score = nil  -- A2MetadataScore
	
	for i,score in ipairs(A2Metadata.get_data().scores) do
		if tostring(score.local_id) == json["local_score_id"] then
			A2Print.print("Updated local score " .. tostring(json["local_score_id"]) .. " to web id " ..  tostring(json["score_id"]) )
			score.web_id = json["score_id"]  -- note: score_id tends to come in as 
			found_score = score
			A2Metadata.save()   -- bugfix: do this before cull_submitted_scores, which will then do a load
			break
		end
	end
			
	if found_score ~= nil then
		-- See if the score has replays or mini replays left. If so, it might be time to trigger another web_sync
		local has_replay_file       = found_score.replay_file ~= "" and found_score.replay_file ~= nil and not found_score.replay_file_uploaded
		local has_mini_replay_file = found_score.mini_replay_file ~= "" and found_score.mini_replay_file ~= nil and not found_score.mini_replay_file_uploaded

		if has_replay_file or has_mini_replay_file then
			A2Print.print("Triggering new web_sync for score local ID " .. tostring(found_score.local_id) .. " with web ID " .. (found_score.web_id) .. " so it can upload replays.")
			if _web_sync_single_score(found_score) and A2Settings.AUTO_CULL_SUBMITTED_SCORES then  -- will return "true" if nothing was actually buffered and the score should just be culled.
				A2Metadata.cull_submitted_scores()
			end
		else
			if A2Settings.AUTO_CULL_SUBMITTED_SCORES then -- no reason to keep this submission around and bloat the user's hard drive, cull it
				A2Metadata.cull_submitted_scores()
			end
		end
		
		A2Metadata.save()
	else
		A2Print.print("Could not find local score with ID: " .. tostring( json["local_score_id"]) )
	end
		
	-- return the parsed json payload
	_got_web_return(A2WebReturn.new(func_name, 1.0, nil, json))
end	
	


function _complete_web_put_replay(response_code, body)
	local func_name = "web_put_replay"
	local generic_wr = _generic_complete_web(response_code, body, func_name)
	if generic_wr ~= nil then  -- something basic went wrong
		_got_web_return( generic_wr )
		return
	end
	
	local txt = body
	A2Print.print("Response for post replay: " .. txt )
	local json = json.decode(txt)
	
	if json == nil or json["mini_replay"] == nil or json["local_score_id"] == nil then
		if json ~= nil  and  json["message"] ~= nil then
			local error = A2ErrorReturn.new(func_name, A2ErrorReturn.A2ErrorCode.BAD_REQUEST, "Server error. Please try again later.", "Error: \"" .. json['message'] .. "\" Check payload for details ")		
			_got_web_return(A2WebReturn.new(func_name, 1.0, error, body))
		else
			_got_web_return(A2WebReturn.json_parse_error(func_name))
		end
	end

	
	-- Search for the local_score_id in our scores, then update its replay finished
	local local_score_id_str = json["local_score_id"]
	local mini_replay = json["mini_replay"] -- tells us whether it was a mini replay or a full replay
	
	local found_score = false
	for i,score in ipairs(A2Metadata.get_data().scores) do
		if tostring(score.local_id) == local_score_id_str then
			A2Print.print("Updated local score" .. local_score_id_str .. " to replay success")
			if mini_replay then
				score.mini_replay_file_uploaded = true
			else
				score.replay_file_uploaded = true
			end
			found_score = true
			break
		end
	end
	
	if found_score then
		A2Metadata.save()   -- otherwise the mini_replay_file_uploaded from above will get clobbered
		if A2Settings.AUTO_CULL_SUBMITTED_SCORES then -- no reason to keep this submission around and bloat the user's hard drive, cull it
			A2Metadata.cull_submitted_scores()
		end
		A2Metadata.save()
	else
		A2Print.print("Could not find local score with ID: " .. local_score_id_str)
	end

	-- return the parsed json payload
	_got_web_return(A2WebReturn.new(func_name, 1.0, nil, json, headers))
end


