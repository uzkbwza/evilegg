-- internal score posting function.
-- score is A2MetadataScore
function _web_post_score(score)
	local func_name = "web_post_score"
	
	local tokens = {} -- array
	for i,username in ipairs(score.usernames) do
		table.insert(tokens, _get_token(username))
	end
	
	
	
	-- Prepare regular form data as a dictionary
	local form_data = {
		usernames= A2Util.comma_delimited_str(score.usernames),
		tokens= A2Util.comma_delimited_str(tokens),
		device_id= M.get_machine_id(),
		game_nickname= A2Settings.GAME_NAME .. "~" .. A2Settings.GAME_VERSION,
		level_nickname= score.level,
		score= score.score,
		frame_length= score.frame_length,
		achievements= score.run_achievements,
		subscores= score.subscores,
		date= score.date,
		meta_str= score.meta_str,
		replay_name= score.replay_name,
		hash= score.my_hash,
		local_score_id= score.local_id  -- for convenience, this will be echoed back
	}	
	
	-- Include the replay file hashes. Binary data will be uploaded in a separate PUT.
	if score.replay_file_hash then
		form_data["replay_file_hash"] = score.replay_file_hash
	end
	if score.mini_replay_file_hash then
		form_data["mini_replay_file_hash"] = score.mini_replay_file_hash
	end
	

	-- Create our body as JSON
	local body = json.encode(form_data)
	
	A2Print.print("Posting score with local_id: " .. tostring(score.local_id) )

	-- Format and send request
	_new_request(func_name)
	local callback = _G["_complete_" .. func_name]
	https.request({A2Settings.ARCADE2_BASEURL .. "a2/api/submit-score/", {
			headers = {["Content-Type"] = "application/json"},
			data = body,
			method = "POST"}}, callback)
end


-- score is A2MetadataScore
-- mini_replay is bool (says whether it's a normal replay or a mini replay)
function _web_put_replay(score, mini_replay)
	if mini_replay == nil then mini_replay = false end

	local func_name = "web_put_replay"
	
	if score.web_id == "" then
		A2Print.print("Score" .. tostring(score.local_id) .. " was missing web id for video upload. Will try again on next web sync.")
		return
	end

	
	local filename = score.replay_file
	if mini_replay then filename=score.mini_replay_file end
	if not love.filesystem.exists( filename ) then
		A2Print.print("Score " .. tostring(score.local_id) .. " was missing replay file for replay upload.")
		return
	end
	
	local tokens = {} -- array
	for i,username in ipairs(score.usernames) do 
		table.insert(tokens, _get_token(username))
	end
	
	local url = A2Settings.ARCADE2_BASEURL .. "a2/api/upload-replay/score/" .. score.web_id .. "/"
	if mini_replay then
		url = A2Settings.ARCADE2_BASEURL .. "a2/api/upload-mini-replay/score/" .. score.web_id .. "/"
	end
	
	local replay_file_bytes = love.filesystem.read( filename )
	
	-- Format and send request
	_new_request(func_name)
	local callback = _G["_complete_" .. func_name]
	local headers = {
		["Content-Type"]="application/octet-stream", 
		["Usernames"] = A2Util.comma_delimited_str(score.usernames),
		["Tokens"] = A2Util.comma_delimited_str(tokens),
		["DeviceId"] = M.get_machine_id(),
		["LocalScoreId"] = tostring(score.local_id)
		}

	https.request({url, {
			headers = headers,
			data = replay_file_bytes,
			method = "PUT"}}, callback)
end



