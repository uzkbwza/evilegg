function _complete_web_check_usernames(response_code, body)
	local func_name = "web_check_usernames"
	local generic_wr = _generic_complete_web(response_code, body, func_name)
	if generic_wr ~= nil then  -- something basic went wrong
		_got_web_return( generic_wr )
		return
	end
	
	-- parse JSON
	local txt = body
	local json = json.decode(txt)
	if json == nil  or  json["user_info"] == nil  or  #json["user_info"]==0 then
		_got_web_return(A2WebReturn.json_parse_error(func_name))
		return
	end
	
	-- stash tokens, if they were acquired
	for user_info in json['user_info'] do
		if user_info['username'] ~= nil  and user_info['token'] ~= nil and user_info['token'] ~= "" then
			A2Metadata.get_data().tokens[user_info['username']] = user_info['token']
		end
		A2Metadata.save()
	end
	
	-- return the parsed json payload
	_got_web_return(A2WebReturn.new(func_name, 1.0, nil, json)) 
end
	

function _complete_web_check_username(response_code, body)
	local func_name = "web_check_username"
	local generic_wr = _generic_complete_web(response_code, body, func_name)
	if generic_wr ~= nil then  -- something basic went wrong
		_got_web_return( generic_wr )
		return
	end
	
	-- parse JSON
	local txt = body
	local json = json.decode(txt)
	if json == nil  or json["user_info"] == nil  or #json["user_info"]==0 then
		_got_web_return(A2WebReturn.json_parse_error(func_name))
		return
	end
		
	-- The parsed json payload from this endpoint will have an array in the "user_info" slot,
	-- but since we only requested one user, we can remove that complication.
	json["user_info"] = json["user_info"][1]
	
	-- stash token, if it was acquired
	local user_info = json["user_info"]
	if user_info['username'] ~= nil  and user_info['token'] ~= nil and user_info['token'] ~= "" then
		A2Metadata.get_data().tokens[user_info['username']] = user_info['token']
		A2Metadata.save()
	end
	
	_got_web_return(A2WebReturn.new(func_name, 1.0, nil, json))
end


