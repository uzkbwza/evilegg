-- Does a GET request to check if a username and password is allowed on the server.
--
-- You still need to do set_user after this to choose the logged in user, assuming you got
-- "authentic" or "available" in the response.
--
-- If a username does not exist, it is automatically created with the supplied password
-- (using supplied email for forgotten passwords if available).
-- If password is not supplied, either device id or stored token will be used.
-- So, once a user is successfully logged in once, you don't really need to worry about it
-- again (the stored token is valid until they log out or change their password).
-- Blank passwords are OK -- for these users, this device's unique ID is used as a password.
-- This will no longer work once they set their own password.
-- Payload returns dictionary in this format: 
--
--    Response gives a "code" result for each user:
--    {
--        "success":true/false,
--        "user_info":
--                {
--                "username":"username",
--                "code":"authentic/created/wrong_password/wrong_device_id/invalid_username/username_banned/device_id_banned",
--                "token": "token" or "" if not authenticated
--                },
--                ...
--    }
function M.web_check_username(username, password, email)
	if password == nil then password = "" end
	if email == nil then email = "" end

	local token = _get_token(username)
	
	_web_check_usernames_internal({{username=username, password=password, token=token, email=email}}, "web_check_username")
end


-- Does a GET request to check if a list of usernames and passwords are allowed on the server.
