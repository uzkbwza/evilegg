-- Does a GET request to get a page of the top scores, starting from the very top.
-- When A2Web.get_web_return("web_get_scoreboard") returns A2WebReturn, 
-- A2WebReturn.payload will contain an A2Scoreboard.
--
-- [param scoreboard_query]: A2ScoreboardQuery format. Various parameters, including pagination settings. See [class A2ScoreboardQuery]. 
-- [param level]: level identifier. Blank is a valid identifier and is the default for one-level games[br][br]
-- [param player_count]: Every unique player count / level combination has its own scoreboard.[br][br]
-- [param relative_to_user]: 
--	  If true, 0 is centered around the currently logged in player (or team's) score, -1
--    is the first page above this one in the chart, and 1 is the first page below.
--    If multiple users are logged in, the scores that are close to the top score of the logged
--    in "team" (specific combination of players). If per_page is 100, you'll get 50 scores above.
--    To use something different than currently logged in players, set centered_on in scoreboard_query.
-- [br][br][br]
--
-- Note that, once the on_web_get_scoreboard signal fires, assuming no error, [member A2WebReturn.payload] will contain
-- a Dictionary-based return that was parsed from the raw JSON scoreboard. It'll have the following structure:[br][br]
-- [codeblock]
-- total_pages: total pages in the scoreboard  
-- total_scores: total scores in the scoreboard 
-- first_page: if you're not using relative_to_user, it'll be 0. Otherwise, it'll be the 0-or-negative index of the first
-- 		page, since the 0 page would be centered on the logged in user(s).
-- zero_page_offset: if not using relative_to_user, it would be 0. Otherwise, this is the first place returned in page 0.
-- per_page: convenience return of your requested per_page amount. 
-- current_page: convenience return of your requested page_num. 
-- previous_page: index of previous page if it exists, or -1 if we're on the first page.
-- next_page: index of next page if it exists, or -1 if we're on the last page.
-- scores: array score chart. Each entry is a Dictionary with the following:
-- 		place: zero-based place in the chart (0 is first place)
-- 		id: id of this score entry in arcade2000's database
-- 		score: the uint64 score (some conversion may be necessary to your desired format)
-- 		users: array of Dictionaries of the users team. will only have one entry in single player. Has the following:
-- 				username: the user's usename.
--				score_sig: if [member A2ScoreboardQuery.get_score_sig] was true, this is a custom string of text that
-- 					the user can append to their name which can include Unicode and emojis (Godot's default font
-- 					supports this).
--				score_sig_img: if [member A2ScoreboardQuery.get_score_sig_img] was true, this is a URL to an image that
-- 					the user uploaded to be their scoreboard sig. WARNING! User-uploaded images.
-- 				discord_id: if [member A2ScoreboardQuery.get_discord_id] was true, this is the user's raw Discord id.
