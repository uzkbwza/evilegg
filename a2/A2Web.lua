-- Checks for common error response codes, returns A2WebReturn if it's something
-- obvious like 404.
-- By default will make an error if it's anything other than 200, override required_response_code to
-- something else or set it to -1 if you have multiple possible response codes
function _generic_complete_web(response_code, body, func_name, required_response_code, call_info)
	if required_response_code == nil then required_response_code = 200 end
	if call_info == nil then call_info = {} end


	A2Print.print(func_name .. " request complete")
	local error = nil
	if response_code == 404 then
		love.filesystem.write("django_error.html", body)	
		error = A2ErrorReturn.new(func_name, A2ErrorReturn.A2ErrorCode.CONNECTION_ERROR, "Connection error. Please check your internet and try again.", "Error 404: not found. Check payload for details.", call_info)
	elseif response_code == 400 then
		-- Bad request, possibly due to invalid data sent to the server
		local body_json = json.decode(body)
		if body_json ~= nil  and  body_json['error'] ~= nil then
			error = A2ErrorReturn.new(func_name, A2ErrorReturn.A2ErrorCode.CONNECTION_ERROR, "Server error. Please try again later.", "Error: \"" .. body_json["error"] .. "\" Check payload for details ", call_info)
		else
			error = A2ErrorReturn.new(func_name, A2ErrorReturn.A2ErrorCode.BAD_REQUEST, "Bad request. Please try again later.", "Error 400: bad request. Check payload for details.", call_info)
		end
	
	elseif response_code == 500 then
		if string.sub(body, 1, 1) == '<' then   -- HTML error response from Django
			love.filesystem.write("django_error.html", body)			
			error = A2ErrorReturn.new(func_name, A2ErrorReturn.A2ErrorCode.CONNECTION_ERROR, "Server error. Please try again later.", "Server error. Django response saved to " .. love.filesystem.getSaveDirectory() .. "/django_error.html", call_info)
		else
			local success, body_json = pcall( json.decode, body)
			if success and body_json ~= nil and body_json['error'] ~= nil then
				error = A2ErrorReturn.new(func_name, A2ErrorReturn.A2ErrorCode.CONNECTION_ERROR, "Server error. Please try again later.", "Error: \"" .. body_json["error"] .. "\" Check payload for details ", call_info)		
			else
				error = A2ErrorReturn.new(func_name, A2ErrorReturn.A2ErrorCode.CONNECTION_ERROR, "Server error. Please try again later.", "Error 500: server error. Check payload for details ", call_info)		
			end
		end
	elseif response_code == 0 then
		error = A2ErrorReturn.new(func_name, A2ErrorReturn.A2ErrorCode.CONNECTION_ERROR, "Couldn't connect to server. Please try again later.", "Error 0: connection error (likely no internet or the server is down) ", call_info)	
	elseif required_response_code ~= -1  and  response_code ~= required_response_code then
		error = A2ErrorReturn.new(func_name, A2ErrorReturn.A2ErrorCode.CONNECTION_ERROR, "Unknown error. Please try again later.", "Response code " .. tostring(required_response_code) .. " expected, but got " .. tostring(response_code) .. ". Check payload for details", call_info)
	end

	if error ~= nil then
		A2Print.print(func_name .. " request failed: " .. error.detailed_description)
		return A2WebReturn.new(func_name, 1.0, error, body, {}, call_info)
	end
		
	return nil
end


