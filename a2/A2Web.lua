-- Intended to be called during a run to keep track of what achievements the user(s) got.
--
-- arcade2000 has three types of achievements, RUN, ONESHOT, and INFO, all of which can be submitted
-- through this function.
-- 
-- RUN achievements are for arcade-style achievements that you would want to keep track of for every
-- single run. For example, "used super bomb", "drove backwards," "hit every tree" etc.
-- By default these are displayed in the scoreboards and you can filter by them. 
--
-- ONESHOT achievements are for console-style achievements that you only get once (e.g. "beat 40
-- levels", etc). If the achievement happens outside of a run, you can instead use
-- A2Web.register_oneshot_achievement. By default these aren't displayed in the scoreboards.
-- There might not be much point in filtering a scoreboard by them, though it is allowed.
--
-- INFO achievements are for additional information about the run you might want to filter by,
-- e.g. "used keyboard," "used gamepad," "playing on Windows," etc. By default these aren't displayed
-- in the scoreboards, though you could still filter by them if desired.
--
-- You're required to create an achievement on the arcade2000 server that matches this achievement_name!
--
-- It's recommended you supply frame_time so you know how far into the run they were when they got this.
-- This is also used to somewhat accurately calculate their lifetime playtime when they got the achievement (based on a sum
-- of frame_lengths of all previously submitted scores)
-- 
-- username: if not supplied, it will default to all logged in users from set_user or set_multi_users
function M.register_run_achievement(achievement_name, frame_time, username )
    if username == nil then username = "" end

	local usernames = {} -- array
	if username ~= "" then
		usernames = {username}
	else
		-- all logged in users
		usernames = M.get_users()
    end
	
	--if len(usernames) == 0:
		--push_error("No users available for register_run_achievement. You need to call web_check_username, followed by and set_user (note you only have to do this once while the game is online, token/login will be stored for future game launches)")
		--return
	-- it's OK to push with no user? if so just do ""
	if #usernames == 0 then
		usernames = {""}
    end
	
	for i, u in ipairs(usernames) do
		local ra = A2MetadataRunAchievement.new()
		ra.username = u
		ra.frame = frame_time
		ra.achievement = achievement_name
		table.insert(M.achievements_this_run, ra)
    end
end



-- It is possible to lost an achievement during a run, e.g. an item which gets dropped.
-- We can still record this as part of the history of the run. The achievement won't be shown
-- in the chart, count towards filters, or the amount of achievements owned, but it can
-- be seen as part of the history of the run in score details.
-- You don't need to supply frame_achieved or username, unless you have multiple of the same
-- achievement that you need to distinguish between.
-- achievement_name is String
-- frame_lost is int
-- frame_achieved is int (optional)
-- username is String (optional)
function M.register_run_achievement_loss(achievement_name, frame_lost, frame_achieved, username)
	if frame_achieved == nil then frame_achieved = -1 end
	if username == nil then username = "" end

	for i,ach in ipairs(M.achievements_this_run) do
		if ach.achievement == achievement_name and (frame_achieved==-1 or ach.frame == frame_achieved) and (username=="" or ach.username==username) then
			ach.frame_lost = frame_lost
			return
		end
	end

	print("WARNING: Couldn't lose run achievement with name " .. achievement_name .. ". A matching prior register_run_achievement call wasn't found.")
	return
end
		
	




