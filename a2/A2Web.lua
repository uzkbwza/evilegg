-- Intended to be called at the end of a playthrough.
--
-- Registers a score (and, optionally, subscores) with the currently logged in user(s).
-- This is local and works offline. You need web_sync to actually
-- submit it to the high score chart on arcade2000.io. 
--
-- Even if you don't have replays, it's recommended to put in a proper frame_length
-- for your playthrough as it's used to calculate the lifetime playtime of users,
-- and the lifetime playtime when they get achievements.
--
-- It will return a unique replay ID, so you can do stuff with the replay
-- after you register the score (rename it, create a clip etc).
--
-- If your game uses replays, you should supply a path to the replay
-- file in "replay_file". This file's binary signature will
-- be used in hashing, to ensure that the score is invalid unless
-- submitted with this specific replay file. If no replay is supplied,
-- your game doesn't use replays, and you're just submitting the
-- number and the username (in a secure way which has basic hash
-- checks to ensure it can't be easily spoofed).
--
-- It's recommended that you register every score and every replay.
-- Being able to see all your players' replays is extremely handy
-- for playtesting and bugfixes.
--
-- Note that this actually makes a copy of the replay when called since the
-- replay file has to hang around until it's successfully uploaded. At that
-- point the duplicate replay will be deleted. So, you might supply something
-- like "last_run.replay" and it's fine. You can delete that file later,
-- or replace it with something else and call this function again, and it
-- will still work, even if not all replays have been uploaded yet.
--
-- "score" is a 64-bit int (default precision for Lua ints).
-- If you game has float scores, it's recommended to just multiply them
-- by something before submitting it, and set the score divisor in your
-- arcade2000 game settings on the website.
-- As another example, if you have a game where the score is a time,
-- you'd submit it in hundredths of a second, set the time
-- format in your arcade2000 game settings on the website, and be sure
-- to check "lower is better" if that's true for your game.
-- If your game has special needs here, please contact admin@arcade2000.io .[br][br]
--
-- "level" is an identifier for the level the score was achieved on.
-- For games with only one level, you can use "" as the main level. This
-- doesn't prevent you from using other level IDs.
--
-- However, if some levels differ from the default (e.g. a lower score is better),
-- even though this isn't the case in your game's default settings, you'll have
-- to create and configure that level ID explicitly on arcade2000.io
--
-- If your game has some level of cheat detection (for example, checking system clock
-- at the beginning and end of a run), you can call this function with cheating_detected=true.
-- This will subtly alter the security hash in A2Settings.A2_DATA_FILE so it will
-- appear to be legitimately registered, but the score will ultimately be rejected when they
-- try to submit to the server, and it will be filed under suspected cheating.
--
-- If desired you could add any additional info about the cheating (or anything else) in meta_str,
-- just remember players can view (though not change) this string if they open the JSON file.
--
-- "subscores" is a list of A2MetadataSubscore, optional additional metrics (besides the main score) that you want
-- 	to keep track of, let score entries be sorted by, etc. You must create a valid "Sub score identifier"
--  on the arcade2000 server to use this. Optionally, they can be hidden from charts and used by
--  the developer only.
--  In multiplayer games, these can optionally apply to a specific player (e.g. "enemies killed" on a per-player
--  basis, "shots fired", etc)
function M.register_score(score, frame_length, level, replay_file, mini_replay_file, 
        subscores, meta_str, cheating_detected)-- ->int:
    if level == nil then level = "" end
    if replay_file == nil then replay_file = "" end
    if mini_replay_file == nil then mini_replay_file = "" end
    if subscores == nil then subscores = {} end
    if meta_str == nil then meta_str = "" end
    if cheating_detected == nil then cheating_detected = false end



	if #M.get_users() == 0 then
		A2Print.print("Buffering score submission until user logs in.")
		buffered_score_submission = {}
		buffered_score_submission["score"] = score
		buffered_score_submission["frame_length"] = frame_length
		buffered_score_submission["level"] = level
		buffered_score_submission["replay_file"] = replay_file
		buffered_score_submission["mini_replay_file"] = mini_replay_file
		buffered_score_submission["subscores"] = subscores
		buffered_score_submission["meta_str"] = meta_str
		buffered_score_submission["cheating_detected"] = cheating_detected
		return -1
    end
	
	
	local submission = A2Metadatascore.new()
	
	-- compute replay file hashes now.
	-- they will also factor into the overall hash, so they can't be changed.
	if replay_file ~= "" then
		if not love.filesystem.exists(replay_file) then
			print("ERROR: register_score: replay file doesn't exist: " .. replay_file)
			return -1
        end
		
		submission.replay_file_hash = A2Util.file_md5(replay_file)
		
		-- make a copy of the file. it will have to stick around until the score has been confirmed submitted,
		-- then it will be deleted by A2Metadata during score cull.
		local new_file = submission.replay_file_hash .. ".replay"

		love.filesystem.write(new_file, love.filesystem.read(replay_file))
		replay_file = new_file   -- forget about the supplied filename
    end

	if mini_replay_file ~= "" then
		if not love.filesystem.exists(mini_replay_file) then
			print("ERROR: register_score: mini replay file doesn't exist: " .. mini_replay_file)
			return -1
        end
					
		submission.mini_replay_file_hash = A2Util.file_md5(mini_replay_file)
		
		local new_file = submission.mini_replay_file_hash .. ".minireplay"
		love.filesystem.write(new_file, love.filesystem.read(mini_replay_file))
		mini_replay_file = new_file   -- forget about the supplied filename
    end
	
	-- let's just build the hash string first.
	-- needs to be built the exact same way that the server will
	local hashstr = ""
	for i, username in ipairs(M.get_users()) do
		hashstr = hashstr .. md5( username)
    end
	
	if not cheating_detected then
		hashstr = hashstr .. md5(  M.get_machine_id() )
	else 
		hashstr = hashstr .. md5( md5(M.get_machine_id()))  -- subtle corruption if cheating detected. the backend knows about this specific hashing method and looks for it
	end  

	hashstr = hashstr .. md5( A2Settings.GAME_NAME .. "~" .. A2Settings.GAME_VERSION)
	hashstr = hashstr .. md5( level)
	hashstr = hashstr .. md5( tostring(score))
	hashstr = hashstr .. md5( tostring(frame_length))
	
	local achievement_str = json.encode(M.achievements_this_run)  -- save this exact string as it's a hash source
	hashstr = hashstr .. md5( achievement_str)
	
	local subscores_str = json.encode(subscores)
	hashstr = hashstr .. md5(subscores_str)
	
	hashstr = hashstr .. md5(submission.date)   -- date comes prepopulated from new()
	hashstr = hashstr .. md5(meta_str)
	if submission.replay_file_hash ~= "" then
		hashstr = hashstr .. md5(submission.replay_file_hash)
    end

	if submission.mini_replay_file_hash ~= "" then
		hashstr = hashstr .. md5(submission.mini_replay_file_hash)
    end

	hashstr = hashstr .. md5(A2Settings.GAME_SECRET_KEY)
	
	
	submission.my_hash = md5(hashstr)  -- all the hashes get hashed together in the end
	submission.usernames = M.get_users()
	submission.score = score
	submission.frame_length = frame_length
	submission.run_achievements = achievement_str
	submission.subscores = subscores_str
	submission.level = level
	submission.meta_str = meta_str
	submission.replay_file = replay_file
	submission.mini_replay_file = mini_replay_file
	submission.web_id = ""  -- will be blank until it's synced successfully
	submission.local_id = A2Metadata.new_local_score_id()
	
	table.insert(A2Metadata.get_data().scores, submission )   -- append the data, when we get to web_sync we'll submit everything to server
	A2Metadata.save()
	
	-- Clear this run's achievements
	M.achievements_this_run = {} -- array
	
	return submission.local_id
end


