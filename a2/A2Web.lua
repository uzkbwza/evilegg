-- The most important web_ function, this will take all the local scores, replays, clips and achievements that
-- you have set up, and synchronize them with the server. 
--
-- So, the player doesn't have to be online while they play the game. They can even create and render clips of
-- their replays. We just do web_sync whenever they connect to the internet to catch up, and the hashes within
-- A2Settings.A2_DATA_FILE ensure none of the data was spoofed. 
--
-- upload_non_pbs: (implementation TBD) This defaults to true, because all replays are useful to you as a developer
-- for playtesting and bugfixes. It also allows for filtered leaderboards, where the top scores under certain
-- conditions may not be the top overall. But if you want to keep uploads to a minimum, you can set this to false,
-- and only the absolutely necessary replay files (the ones attached to personal bests) will upload.
--
-- video_upload: Note that if you want to actually upload a generated clip video which has been attached to a clip via
-- A2Web.register_clip or A2Web.modify_clip, you must explicitly set video_upload to
-- true, because video uploads could take a while. While web_sync can happen invisibly in the background most of
-- the time, you might want to use A2Web.get_web_return to display a progress bar when you do 
-- video_upload=true, since otherwise there's a good chance they'll quit the game before it even finishes
-- uploading.
function M.web_sync(_upload_non_pbs, video_upload)
	if _upload_non_pbs == nil then _upload_non_pbs = true end
	if video_upload == nil then video_upload = false end

	
	-- iterate through scores, checking for blank web_id
	local should_cull = false
	for i, score in ipairs(A2Metadata.get_data().scores) do
		should_cull = should_cull or _web_sync_single_score(score)  -- returns true if score is cullable
	end
	
	if should_cull and A2Settings.AUTO_CULL_SUBMITTED_SCORES then  -- any score was cullable?
		A2Metadata.cull_submitted_scores()
	end
	
	if A2Metadata.get_data().clips ~= nil then
		for i, clip in ipairs(A2Metadata.get_data().clips) do
			_web_sync_single_clip(clip, video_upload)
		end
	end
end

