-- This version of a2love doesn't have a way to get a device fingerprint,
-- this function should be called externally from a function that DOES have a unique uuid,
-- before using a2web.
function M.set_machine_id(machine_id)
    A2Metadata.get_data().machine_id = machine_id
    A2Metadata.get_data().machine_id_hash = md5( machine_id .. md5( A2Settings.GAME_SECRET_KEY))
    A2Metadata.save()
end



-- Machine id is not permanent
-- So the first time we fetch it, we just store it in [member A2Settings.A2_DATA_FILE]
-- and keep it forever. If the user's hard drive got stolen, the thief could submit scores
-- in their name until they change their password, but the thief would still not be able
-- to log into their account on the website. Tokens are only valid for score submission.
function M.get_machine_id() ---- ->String:
    local data = A2Metadata.get_data()
	-- Invalidate machine id if the hash doesn't match (likely the user changed it themselves in the json file)
	if (data.machine_id ~= ""  and  data.machine_id ~= nil and
		    data.machine_id_hash ~= md5( data.machine_id .. md5( A2Settings.GAME_SECRET_KEY))) then
		data.machine_id = ""  -- invalidate
    end
		
	if data.machine_id == "" or data.machine_id == nil then
        print("ERROR: no machine ID found. Make sure you called a2web.set_machine_id")
    end
		
	return data.machine_id
end
	



