-- Sets the main username internally. It will be saved in A2Settings.A2_DATA_FILE and
-- loaded automatically, so it doesn't need to be set every time the game runs.
-- If username doesn't exist, it will be created next web_sync.
-- If it does exist, but password is wrong, next web_sync will fail (unless frictionless
-- usernames are enabled for the game)
-- All previously signed in users are stored in A2Settings.A2_DATA_FILE
-- (the actual password is not stored on the hard drive).
--
-- username: Should be non-blank, but doesn't need to be pre-registered.
-- If the username is unique, it'll be created next web_sync.
--
-- password: is allowed to be blank. In this case, machine ID will be used
-- until they create a password. If the username exists, this must match the online
-- password. If the username doesn't exist, this is used to create password.
--
-- email: is allowed to be blank. It is only if this username doesn't exist
-- and must be auto-created, that this parameter will be used to store their
-- email in case they forget their password.
--
-- You can use web_check_username if you want to explicitly check if a
-- username / password combination is valid. set_user doesn't call out
-- to the web, so there is no return value, and no way to know if the username
-- was valid until you make a web request.
--
-- Note that password is not actually stored on the hard drive, only a device-specific
-- login token is. So, if you set username and password but quit out of the game without
-- ever doing web_sync, password will be required again the next time they play.
-- But if you do web_sync after calling set_user, their device-specific token will
-- be stored indefinitely (even if you set_user to something different) until
-- you explicitly erase it with logout_user.
function  M.set_user(username, password, email)
	M.set_multi_users({{username=username, password=password, email=email}})
end


